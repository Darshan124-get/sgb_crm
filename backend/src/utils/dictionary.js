/**
 * Helper to get a localized string
 * @param {string} lang - 'en', 'kn', 'hi', etc.
 * @param {string} key 
 * @param {object} params - { name: '...', product: '...' } 
 */
const getMsg = (lang, key, params = {}) => {
  const language = dictionary[lang] || dictionary['en'];
  let message = language[key] || dictionary['en'][key] || key;

  // Simple parameter replacement
  Object.keys(params).forEach(p => {
    message = message.replace(`{${p}}`, params[p]);
  });

  return message;
};

module.exports = {
  dictionary,
  getMsg
};
